package src;


public class Giocatore {
	
	int frecce;
	int stanza;
	
	public Giocatore(int numFrecce, int stanza) {
		this.frecce=numFrecce;
		this.stanza=stanza;
	}
	
	public int getFrecce() {
		return this.frecce;
	}
	
	public int getStanza() {
		return this.stanza;
	}
	
	public void setStanza(int stanza) {
		this.stanza=stanza;
	}
	
	public void eliminaFreccia() {
		this.frecce--;
	}
	
}
